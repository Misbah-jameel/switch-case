// Switch cases
var grade = "a";
switch (grade) {
    case "A":
        console.log("You are bright student");
        break;
    case "B":
        console.log("second");
        break;
    case "C":
        console.log("Third");
        break;
    case "D":
        console.log("fail");
        break;
    default:
        console.log("invalid output");
}
